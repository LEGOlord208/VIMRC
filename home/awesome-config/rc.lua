local function set_wallpaper(s)
    -- Wallpaper
    if beautiful.wallpaper then
        local wallpaper = beautiful.wallpaper
        -- If wallpaper is a function, call it with the screen
        if type(wallpaper) == "function" then
            wallpaper = wallpaper(s)
        end
        gears.wallpaper.maximized(wallpaper, s, true)
    end
end

-- Re-set wallpaper when a screen's geometry changes (e.g. different resolution)
screen.connect_signal("property::geometry", set_wallpaper)

awful.screen.connect_for_each_screen(function(s)
    -- Set padding
    s.padding = beautiful.padding;

    -- Wallpaper
    set_wallpaper(s)

    -- Set up a wibar for tasklists
    s.wibar = awful.wibar({
        position = "left",
        screen = s,
        visible = false,
        width = 200,
    })
    s.wibar:setup {
        layout = wibox.layout.stack,
        {
           awful.widget.tasklist {
              screen  = s,
              filter  = awful.widget.tasklist.filter.currenttags,
              buttons = tasklist_buttons,
              layout  = wibox.layout.fixed.vertical(),

              widget_template = {
                 {
                    {
                       id     = "text_role",
                       widget = wibox.widget.textbox,
                    },
                    widget = wibox.container.margin,
                    top    = 5,
                    bottom = 5,
                 },
                 id     = "background_role",
                 widget = wibox.container.background,
              },
           },
           widget = wibox.container.margin,
           top = 50,
        },
    }

    -- Each screen has its own tag table.
    awful.tag({ "1", "2", "3", "4", "5", "6", "7", "8" }, s, awful.layout.suit.spiral)
    awful.tag.add("9", {
        screen = s,
        layout = awful.layout.suit.max,
    })

    for _, t in ipairs(s.tags) do
      local callback = function(tag)
          s.wibar.visible = (tag.layout == awful.layout.suit.max)
      end
      t:connect_signal("property::selected", callback)
      t:connect_signal("property::layout", callback)
    end
end)
